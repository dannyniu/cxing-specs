<?= hc_H1("Library for Time") ?>

```
subr time();
```

The `time` function returns the number of seconds elapsed since
`1970-01-01 T 00:00:00 +0000 (UTC)`, as an integer.

**Note** (2026-06-21): Until such time when demand for additional time facilities
becomes apparent, we refrain from adding half-devised unidiomatic interfaces.
